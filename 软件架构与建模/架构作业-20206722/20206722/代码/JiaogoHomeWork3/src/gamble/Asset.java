
package gamble;

public class Asset {

	private String assertName;
	
	private int value;

	
	public String getAssertName() {
		return assertName;
	}

	public void setAssertName(String assertName) {
		this.assertName = assertName;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}

	public Asset(String assertName, int value) {
		super();
		this.assertName = assertName;
		this.value = value;
	}

	
	
	
	
	
    
   
}
